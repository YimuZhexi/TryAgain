package com.example.tryagain.data;

/**
 * 设置outText类型数据
 */


public class outText {
    private String head;//设置String类型标题
    private String body;//设置String类型类容

    //获得标题类容
    public String getHead() {
        return head;
    }
    //设置标题类容
    public void setHead(String head) {
        this.head = head;
    }
    //获得类容
    public String getBody() {
        return body;
    }
    //设置类容
    public void setBody(String body) {
        this.body = body;
    }
}
